# Script Python que lee tres variables enteras a, b y c
# e imprime a**b**c

a= int(input("El valor de a:"))
b= int(input("El valor de b:"))
c= int(input("El valor de c:"))

r = a**b**c

print(f'El resultado de {a}**{b}**{c}={r}')
    

